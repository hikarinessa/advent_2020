default_move = [3, 1]
tree = "#"


def traverse(move):
    x = 0
    y = 0
    trees = 0
    my_map = data.splitlines()
    max_x = len(my_map[0])
    max_y = len(my_map)
    while True:
        x += move[0]
        y += move[1]
        if y >= max_y:
            break
        elif my_map[y][x % max_x] == tree:
            trees += 1
    return trees


def part_one():
    trees = traverse(default_move)
    print("*" * 96)
    print("PART ONE")
    print("{} trees encountered.".format(trees))


def part_two():
    moves = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]
    result = 1
    for move in moves:
        result *= traverse(move)
    print("*" * 96)
    print("PART TWO")
    print(result)


if __name__ == "__main__":
    part_one()
    part_two()
